"""
================================================================
Comparison Operators
================================================================

`<`: less than
`<=`: less than or equal to
`>`: greater than
`>=`: greater than or equal to
`==`: equality operator
`!=`: inequality operator
"""


x = 3 > 2
# here (3 > 2) expression is called a boolean expression as it returns a boolean value
print(x)

y = 3 <= 2
print(y)

z1 = 3 == 2
print(z1)

z2 = 3 == (9//3)
print(z2)
